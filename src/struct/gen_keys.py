max_iterations = 0
items = set()
buckets = {}
found = set()

def check(value):
  global max_iterations
  global found
  global items
  global buckets

  iterations = 0
  i = 510
  v = value
  while keymap[i][3] == 65535:
    if (v % 2) == 1:
      i = keymap[i][1]
    else:
      i = keymap[i][2]
    
    v //= 2
    iterations += 1

  max_iterations = max(max_iterations, iterations)
  
  bits = f"{value:032b}"[::-1][:iterations]

  if bits not in found:
    found.add(bits)
    
    if iterations not in buckets:
      buckets[iterations] = set()
      
    buckets[iterations].add(bits)

    if keymap[i][3] % 256 < 32:
      return

    char = chr(keymap[i][3] % 256)
    if (char >= 'a' and char <= 'z') or (char >= '0' and char <= '9') or char == ' ':
      items.add(char)
      print(f"{bits} -> {char} ({keymap[i][3]}) in {iterations} iterations")

for i in range(65535):
  check(i)

